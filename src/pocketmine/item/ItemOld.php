	static public function getId($id) {
		if ($id < 256) {
			return $id;
		}
		if (in_array($id, self::DYE_ARRAY)) {
			return self::OLD_DYE;
		}
		$oClass = new \ReflectionClass(__CLASS__);
		$old = $oClass->getConstants();
		$oClass = new \ReflectionClass('pocketmine\item\Item');
		$new = $oClass->getConstants();
		$key = 'OLD_' . array_search($id, $new);

		return $old[$key]??$id;
	}

	static public function getDyeMeta($id) {
		return array_search($id, self::DYE_ARRAY);
	} 

	static public function getNewByOld($id) {
		if ($id < 256) {
			return $id;
		}
		$oClass = new \ReflectionClass(__CLASS__);
		$old = $oClass->getConstants();
		$oClass = new \ReflectionClass('pocketmine\item\Item');
		$new = $oClass->getConstants();
		$key = array_search($id, $old);
		$key = explode('_', $key)[1];

		return $new[$key]??$id;
	}
